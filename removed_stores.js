// 매장이 블랙리스트에 해당하는지 검사
function isBlocked(brand, name) {
  // 1. 정확 매칭
  for (var i = 0; i < REMOVED_STORES.exact.length; i++) {
    var e = REMOVED_STORES.exact[i];
    if (e.brand === brand && (name === e.name || name.indexOf(e.name) !== -1)) return true;
  }
  // 2. 패턴 매칭
  var patterns = REMOVED_STORES.patterns[brand] || [];
  for (var j = 0; j < patterns.length; j++) {
    if (name.indexOf(patterns[j]) !== -1) return true;
  }
  // 3. 까사미아 자체 아울렛 룰
  if (REMOVED_STORES.rules.excludeOwnOutlet && brand === "까사미아") {
    // "아울렛"으로 시작하는 자체 아울렛 (브랜드+백화점 형태가 아닌 경우)
    if (/^아울렛/.test(name)) return true;
  }
  return false;
}

